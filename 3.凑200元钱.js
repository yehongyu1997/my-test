// 3层for循环嵌套在嵌套if语句
// 类似于枚举法
for (var i = 0; i <= 200; i++) {
  //i为1元纸币张数
  for (var j = 0; j <= 100; j++) {
    //j为2元纸币张数
    for (var k = 0; k <= 40; k++) {
      //j为5元纸币张数
      if (i * 1 + j * 2 + k * 4 == 200) {
        console.log(i + "张1元+" + j + "张2元+" + k + "张5元=200元");
      }
    }
  }
}

